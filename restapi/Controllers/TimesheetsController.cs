        private const int SUPERVISOR_ID = 5555;

        [HttpGet]
        [Produces(ContentTypes.Timesheets)]
        [ProducesResponseType(typeof(IEnumerable<Timecard>), 200)]
        public IEnumerable<Timecard> GetAll()
        {
            return Database
                .All
                .OrderBy(t => t.Opened);
        }

        [HttpGet("{id}")]
        [Produces(ContentTypes.Timesheet)]
        [ProducesResponseType(typeof(Timecard), 200)]
        [ProducesResponseType(404)]
        public IActionResult GetOne(string id)
        {
            Timecard timecard = Database.Find(id);

            if (timecard != null) 
            {
                return Ok(timecard);
            }
            else
            {
                return NotFound();
            }
        }

        [HttpPost]
        [Produces(ContentTypes.Timesheet)]
        [ProducesResponseType(typeof(Timecard), 200)]
        public Timecard Create([FromBody] DocumentResource resource)
        {
            // FUTURE: We may want to verify the resource creating the timecard
            // doesn't already have an open timecard. No requirement for this so
            // leaving as-is for now.

            var timecard = new Timecard(resource.Resource);

            var entered = new Entered() { Resource = resource.Resource };

            timecard.Transitions.Add(new Transition(entered));

            Database.Add(timecard);

            return timecard;
        }

        // 1. Remove (DELETE) a draft or cancelled timecard
        [HttpDelete("{timecardId}/delete")]
        [ProducesResponseType(200)]
        [ProducesResponseType(404)]
        [ProducesResponseType(typeof(InvalidStateError), 409)]
        public IActionResult Delete(string timecardId)
        {
            Timecard timecard = Database.Find(timecardId);

            if (timecard == null)
            {
                return NotFound();
            }

            // Verify state allows this action
            if (timecard.Status != TimecardStatus.Cancelled && timecard.Status != TimecardStatus.Draft)
            {
                    return StatusCode(409, new InvalidStateError() { });
            }

            Database.Delete(timecardId);
            // No state transition since the timecard no longer exists
            return Ok();
        }

        // 2. Replace (POST) a complete line item
        [HttpPost("{timecardId}/replace/{lineId}")]
        [Produces(ContentTypes.TimesheetLine)]
        [ProducesResponseType(typeof(AnnotatedTimecardLine), 200)]
        [ProducesResponseType(404)]
        [ProducesResponseType(typeof(InvalidStateError), 409)]
        public IActionResult ReplaceLine(string timecardId, int lineId, [FromBody] TimecardLine timecardLine)
        {
            Timecard timecard = Database.Find(timecardId);

            // Verify timecard exists
            if (timecard == null)
            {
                return NotFound();
            }

            // Verify state allows this action
            if (timecard.Status != TimecardStatus.Draft)
            {
                return StatusCode(409, new InvalidStateError() { });
            }

            // Perform the replacement
            var updatedLine = timecard.ReplaceLine(timecardLine, lineId);

            // Verify line was found and replacement occurred
            if (updatedLine == null)
            {
                return NotFound();
            }

            return Ok(updatedLine);
        }

        [HttpGet("{id}/lines")]
        [Produces(ContentTypes.TimesheetLines)]
        [ProducesResponseType(typeof(IEnumerable<AnnotatedTimecardLine>), 200)]
        [ProducesResponseType(404)]
        public IActionResult GetLines(string id)
        {
            Timecard timecard = Database.Find(id);

            // No need for state validation here since this can be called at any time

            if (timecard != null)
            {
                var lines = timecard.Lines
                    .OrderBy(l => l.WorkDate)
                    .ThenBy(l => l.Recorded);

                return Ok(lines);
            }
            else
            {
                return NotFound();
            }
        }

        [HttpPost("{id}/lines")]
        [Produces(ContentTypes.TimesheetLine)]
        [ProducesResponseType(typeof(AnnotatedTimecardLine), 200)]
        [ProducesResponseType(404)]
        [ProducesResponseType(typeof(InvalidStateError), 409)]
        public IActionResult AddLine(string id, [FromBody] TimecardLine timecardLine)
        {
            Timecard timecard = Database.Find(id);

            if (timecard != null)
            {
                if (timecard.Status != TimecardStatus.Draft)
                {
                    return StatusCode(409, new InvalidStateError() { });
                }

                var annotatedLine = timecard.AddLine(timecardLine, 0);

                return Ok(annotatedLine);
            }
            else
            {
                return NotFound();
            }
        }

        // 3. Update (PATCH) a line item
